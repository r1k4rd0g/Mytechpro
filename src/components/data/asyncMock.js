export const getProducts = (idCategory) => {

    console.log('idCategory desde asyncMok', idCategory)

    const filterList = idCategory
        ? productsList.filter(product => product.category === idCategory)
        : productsList

    return new Promise((res, rej) => {
        setTimeout(() => {
            filterList.length > 0 ?
                res(filterList)
                :
                rej(alert('no hay datos de productos por categoría'))
        }, 1000)
    })
}